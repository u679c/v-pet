
class Food():
    def __init__(self, name, nutrition, happiness, health, price, experience):
        '''
            - name: 食物名
            - nutrition: 营养值
            - happiness: 幸福值
            - health: 健康值
            - price: 价格
            - experience: 经验值
        '''
        self.name = name # 食物名
        self.nutrition = nutrition # 营养值
        self.happiness = happiness # 幸福值
        self.health = health # 健康值
        self.experience = experience
        self.price = price # 价格
    